// 카테고리별 메뉴 필터링 함수
export function filterMenuByCategory(category) {
    if (category === 'all') {
        return menuData;
    }
    return menuData.filter(item => item.category === category);
}

// 메뉴 ID로 메뉴 찾기
export function findMenuById(id) {
    return menuData.find(item => item.id === id);
}
